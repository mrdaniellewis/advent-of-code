# frozen_string_literal: true

lines = []

DATA.each_line do |line|
  lines << line.strip.split("")
end

scores = lines.map do |line|
  history = []
  ok = line.all? do |char|
    if ["{", "(", "[", "<"].include?(char)
      history << char
      next true
    end

    if char == { "{" => "}", "(" => ")", "[" => "]", "<" => ">" }[history.last]
      history.pop
      next true
    end

    false
  end
  next nil unless ok

  history.reverse.reduce(0) do |t, char|
    t * 5 + { "(" => 1, "[" => 2, "{" => 3, "<" => 4 }[char]
  end
end.compact

puts scores.sort[scores.length / 2]

